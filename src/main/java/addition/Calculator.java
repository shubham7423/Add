package addition;

/**
 * Calculator class
 * @author shubhampatel
 *
 */
public class Calculator {
	int a, b;
	
	/**
	 * 
	 * @return addition of numbers
	 */
	public int addNum() {
		return a + b;
		
	}
	
	/**
	 * 
	 * @return subtraction of number
	 */
	public int subtract() {
		return a - b;
	}
	
}
